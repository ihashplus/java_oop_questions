public class Main {
    public static void main(String[] args) {

        int num1 = 5;
        double num2 = -10.5;
        double num3 = 8.3;
        System.out.println("Square of 5 is: " + Math.square(num1));
        System.out.println("Max of -10.5 and 8.3 is: " + Math.max(num2, num3));
        System.out.println("Absolute value of -10.5 is: " + Math.abs(num2));

    }
}